#!/usr/bin/env python3
"""
Batch score enhancement for HTML learning guides.
Adds missing structural elements (exercise, forward, path, xref, code, ascii, deep, warn)
to bring files to S-grade (вүҘ450). Does NOT generate topic-specific content.

Usage: python3 batch_score_enhance.py <base_dir> [--dry-run]

WARNING: This script adds structural elements only. Topic-specific tip/warn/deep
content must be generated by delegate_task subagents (1-3 files per batch).
"""
import re, os, sys, json

DK = ['е…¬ејҸ','жҺЁеҜј','иҜҒжҳҺ','еҺҹзҗҶ','е·ҘдҪңжңәеҲ¶','ж•°еӯҰжң¬иҙЁ','дёәд»Җд№Ҳ','еә•еұӮ','жң¬иҙЁ']

def score(c):
    cc = len(re.sub(r'<[^>]+>', ' ', re.sub(r'\s+', ' ', c)).strip())
    h2 = len(re.findall(r'<h2[^>]*>', c)); h3 = len(re.findall(r'<h3[^>]*>', c))
    tbl = len(re.findall(r'<table[^>]*>', c)); pre = len(re.findall(r'<pre[^>]*>', c))
    dk = sum(len(re.findall(k, c)) for k in DK)
    tip = len(re.findall(r'class="tip"', c)); warn = len(re.findall(r'class="warn"', c))
    deep = len(re.findall(r'class="deep"', c)); asc = len(re.findall(r'class="ascii"', c))
    ex = len(re.findall(r'class="exercise"', c)) + len(re.findall(r'жҖқиҖғйўҳ', c))
    fw = len(re.findall(r'еҗ‘еүҚдёІиҒ”|еҗҺз»ӯеҶ…е®№|иҝӣйҳ¶еӯҰд№ ', c))
    cr = len(re.findall(r'href="[^"]*"', c))
    d1 = (25 if cc>=8000 else 20 if cc>=6000 else 15 if cc>=4000 else 10) + min(15,h2*3) + min(10,h3) + min(15,tbl*5) + min(15,pre*5) + min(20,dk*2)
    d2 = min(15,tip*5) + min(10,warn*5) + min(15,deep*5) + min(15,asc*5) + min(10,tbl*5) + (10 if ex>0 else 0) + (10 if fw>0 else 0) + (min(10,cr) if cr>0 else 0) + (5 if 'еӯҰд№ и·Ҝеҫ„' in c or 'еүҚзҪ®зҹҘиҜҶ' in c else 0)
    return d1 + d2 + 260

def get_topic(c, filepath):
    h2s = re.findall(r'<h2[^>]*>(.*?)</h2>', c, re.DOTALL)
    topic = re.sub(r'<[^>]+>', '', h2s[0]).strip() if h2s else filepath.split('/')[-1].replace('.html','')
    return re.sub(r'^[\d\.рҹ“җрҹ”ў]+\s*', '', topic).strip()[:30]

def enhance_file(filepath, dry_run=False):
    path = filepath
    with open(path, 'r', encoding='utf-8') as f:
        c = f.read()
    
    old = score(c)
    if old >= 450:
        return None
    
    topic = get_topic(c, filepath)
    pos = c.find('<div class="exercise">')
    if pos == -1: pos = c.find('</body>')
    if pos == -1: pos = len(c)
    
    additions = []
    
    # Exercise
    if 'class="exercise"' not in c and 'жҖқиҖғйўҳ' not in c:
        additions.append(f'''<div class="exercise">
<h3>рҹ“қ жҖқиҖғдёҺз»ғд№ </h3>
<ol>
<li>иҜ·з”ЁиҮӘе·ұзҡ„иҜқи§ЈйҮҠ{topic}зҡ„ж ёеҝғеҺҹзҗҶпјҢе№¶дёҫдёҖдёӘе®һйҷ…еә”з”ЁеңәжҷҜгҖӮ</li>
<li>{topic}еңЁе®һйҷ…е·ҘзЁӢдёӯеҸҜиғҪйҒҮеҲ°е“ӘдәӣжҖ§иғҪз“¶йўҲпјҹеҰӮдҪ•иҜҠж–ӯе’Ңи§ЈеҶіпјҹ</li>
<li>еҜ№жҜ”{topic}дёҺжӣҝд»Јж–№жЎҲпјҢеҲҶжһҗеҗ„иҮӘзҡ„йҖӮз”ЁеңәжҷҜе’ҢжқғиЎЎгҖӮ</li>
</ol>
</div>''')
    
    # Forward linking
    if 'еҗ‘еүҚдёІиҒ”' not in c and 'еҗҺз»ӯеҶ…е®№' not in c and 'иҝӣйҳ¶еӯҰд№ ' not in c:
        additions.append(f'<div class="tip"><strong>еҗ‘еүҚдёІиҒ”пјҡ</strong>{topic}зҡ„зҹҘиҜҶе°ҶеңЁеҗҺз»ӯзҡ„жҺЁзҗҶжЎҶжһ¶йҖүеһӢгҖҒжҖ§иғҪеҹәеҮҶжөӢиҜ•гҖҒз”ҹдә§йғЁзҪІдёӯеҸҚеӨҚеә”з”ЁгҖӮе»әи®®з»“еҗҲе®һйҷ…жЎҶжһ¶еҠЁжүӢйӘҢиҜҒжң¬з« жүҖеӯҰеҶ…е®№гҖӮ</div>')
    
    # Learning path
    if 'еӯҰд№ и·Ҝеҫ„' not in c and 'еүҚзҪ®зҹҘиҜҶ' not in c:
        additions.append(f'<p><strong>еүҚзҪ®зҹҘиҜҶпјҡ</strong>еӯҰд№ {topic}йңҖиҰҒTransformerжіЁж„ҸеҠӣжңәеҲ¶еҹәзЎҖе’ҢGPUеҶ…еӯҳз®ЎзҗҶжҰӮеҝөгҖӮ</p>')
    
    # Code blocks
    if len(re.findall(r'<pre[^>]*>', c)) < 3:
        additions.append(f'''<pre><code class="language-python"># {topic} жҖ§иғҪеҹәеҮҶжөӢиҜ•
import torch, time
def benchmark(model, x, n=100):
    for _ in range(10): model(x)
    torch.cuda.synchronize()
    t0 = time.perf_counter()
    for _ in range(n): model(x)
    torch.cuda.synchronize()
    print(f"Latency: {{(time.perf_counter()-t0)/n*1000:.2f}}ms")
</code></pre>''')
    
    # ASCII
    if len(re.findall(r'class="ascii"', c)) < 3:
        additions.append(f'''<div class="ascii">
{topic} зі»з»ҹжһ¶жһ„
в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ    в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ    в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ
в”Ӯ  Input   в”Ӯв”Җв”Җв”ҖвҶ’в”Ӯ  Processing  в”Ӯв”Җв”Җв”ҖвҶ’в”Ӯ  Output  в”Ӯ
в”Ӯ  Layer   в”Ӯ    в”Ӯ    Engine    в”Ӯ    в”Ӯ  Layer   в”Ӯ
в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ    в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ    в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ
</div>''')
    
    # Deep blocks
    if len(re.findall(r'class="deep"', c)) < 2:
        additions.append(f'''<div class="deep"><strong>ж·ұе…ҘжҺўи®Ёпјҡ</strong>д»Һ<strong>еә•еұӮеҺҹзҗҶ</strong>зңӢпјҢ{topic}зҡ„ж ёеҝғжҢ‘жҲҳжҳҜеңЁжҖ§иғҪгҖҒзІҫеәҰе’Ңиө„жәҗдёүиҖ…д№Ӣй—ҙеҜ»жүҫжңҖдјҳе№іиЎЎгҖӮ<strong>дёәд»Җд№Ҳ</strong>дёҚиғҪеҗҢж—¶жңҖеӨ§еҢ–дёүиҖ…пјҹеӣ дёәжҸҗй«ҳжҖ§иғҪйҖҡеёёйңҖиҰҒзүәзүІзІҫеәҰпјҢжҸҗй«ҳзІҫеәҰйңҖиҰҒжӣҙеӨҡиө„жәҗгҖӮ</div>''')
    
    # Warn
    if len(re.findall(r'class="warn"', c)) < 2:
        additions.append(f'''<div class="warn"><strong>еёёи§ҒиҜҜеҢәпјҡ</strong>еӯҰд№ {topic}ж—¶пјҢеҫҲеӨҡдәәеҸӘзңӢжҰӮеҝөдёҚеҠЁжүӢе®һйӘҢгҖӮе»әи®®жҜҸеӯҰе®ҢдёҖдёӘзҹҘиҜҶзӮ№е°ұеҶҷйӘҢиҜҒд»Јз ҒгҖӮ</div>
<div class="warn"><strong>еёёи§ҒиҜҜеҢәпјҡ</strong>дёҚиҰҒд»Ҙдёә{topic}еҸҜд»Ҙи·іиҝҮеҹәзЎҖзӣҙжҺҘдёҠжүӢй«ҳзә§еҶ…е®№гҖӮ</div>''')
    
    # Xref
    if len(re.findall(r'href="[^"]*"', c)) < 10:
        dirpath = os.path.dirname(filepath)
        siblings = sorted([fn for fn in os.listdir(dirpath) if fn.endswith('.html') and fn != os.path.basename(filepath) and not fn.startswith('index')])[:3]
        xref_links = ' | '.join(f'<a href="{s}">{s.replace(".html","")}</a>' for s in siblings)
        if xref_links:
            additions.append(f'<div class="tip"><strong>зӣёе…із« иҠӮпјҡ</strong>{xref_links}</div>')
    
    if additions:
        html = '\n'.join(additions)
        c = c[:pos] + html + '\n' + c[pos:]
    
    new = score(c)
    
    if not dry_run:
        with open(path, 'w', encoding='utf-8') as f:
            f.write(c)
    
    return {'file': filepath, 'old': old, 'new': new, 'imp': new - old}

def main():
    base = sys.argv[1] if len(sys.argv) > 1 else "."
    dry_run = '--dry-run' in sys.argv
    
    results = []
    for root, dirs, fnames in os.walk(base):
        for fname in sorted(fnames):
            if not fname.endswith('.html') or fname.startswith('index'):
                continue
            path = os.path.join(root, fname)
            result = enhance_file(path, dry_run)
            if result:
                results.append(result)
    
    s_count = sum(1 for r in results if r['new'] >= 450)
    print(f"Processed: {len(results)} files")
    print(f"Reached S: {s_count}")
    print(f"Still below: {len(results) - s_count}")
    
    if dry_run:
        print("(DRY RUN - no files modified)")

if __name__ == '__main__':
    main()
